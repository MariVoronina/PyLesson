students = ["Вася", "Маша", "Петя", "Дима", "Марина", "Люба", "Коля", "Ваня"]
grades = {"Вася" : 4, "Петя" : 9, "Марина" : 8, "Люба" : 4, "Коля" : 5, "Ваня": 10}

for i in students:
    if grades.get(i) != None:
        print("Имя студента: ", i)
        print("Оценка студента: ", grades[i])
    else:
        print(i, " контрольную работу не писал(а)")
